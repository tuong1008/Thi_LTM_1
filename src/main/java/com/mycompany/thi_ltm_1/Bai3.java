class Client implements ClientInterface{
        JTextArea jtaToShow;
        
        public Client(JTextArea jtaToShow) throws RemoteException{
            UnicastRemoteObject.exportObject(this, 0);
            this.jtaToShow = jtaToShow;
        }
    
        @Override
        public void receiveMessage(String result) {
            jtaToShow.append(result+"\n");
        }
        
    }
